
H = {0, 2, 4, 6, 8};
S = {1, 2, 3, 4, 5};

# set union
print("Union of H and S is",H | S)

# set intersection
print("Intersection of H and S is",H & S)

# set difference
print("Difference of H and S is",H - S)

# set symmetric difference
print("Symmetric difference of H and S is",H ^ S)
